package EmployeArray;

public class MyDate {
	int dat,month,year;

	public MyDate(int dat, int month, int year) {
		super();
		this.dat = dat;
		this.month = month;
		this.year = year;
	}

	@Override
	public String toString() {
		return "MyDate [dat=" + dat + ", month=" + month + ", year=" + year + "]";
	}
	

}
