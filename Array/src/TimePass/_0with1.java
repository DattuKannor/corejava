package TimePass;

public class _0with1 {
	public static void main(String[] args) {
		int a[]= {26,0,67,4,0,78,54,34,10,0,34};
		int t;
		for (int i = 0; i < a.length; i++) {
			t=0;
			if(a[i]==t && a[i]!=1)
			{
				t++;
				System.out.println(t);
			}
			else
			{
				System.out.println(a[i]);
			}
		}
	}

}
